function buildYoutubeSearchUrl(query) {
	return `https://www.youtube.com/results?search_query=${encodeURIComponent(query)}`;
}

function buildYoutubeResources(question) {
	const [primaryChannel = "takeuforward", secondaryChannel = "neetcode"] =
		YOUTUBE_CHANNEL_HINTS[question.topicId] || [];
	const baseQuery = `${question.title} ${question.platform === "LeetCode" ? "leetcode" : question.platform} coding interview`;

	return [
		{
			title: `${question.title} explained`,
			url: buildYoutubeSearchUrl(`${baseQuery} ${primaryChannel}`),
			channel: primaryChannel,
			duration: "",
		},
		{
			title: `${question.title} alternate explanation`,
			url: buildYoutubeSearchUrl(`${baseQuery} ${secondaryChannel}`),
			channel: secondaryChannel,
			duration: "",
		},
	];
}

function enrichDsaQuestion(question) {
	const tags = Array.from(
		new Set([
			...(question.tags || []),
			question.topicId,
			...(question.pattern
				? question.pattern
						.split("/")
						.map((tag) => tag.trim().toLowerCase())
						.filter(Boolean)
				: []),
		]),
	);

	return {
		...question,
		track: "dsa",
		tags,
		youtubeResources: question.youtubeResources?.length
			? question.youtubeResources
			: buildYoutubeResources(question),
	};
}

const ALL_QUESTIONS = [...QUESTIONS, ...EXTRA_QUESTIONS].map(enrichDsaQuestion);

async function seed() {
	const MONGO_URI =
		process.env.MONGODB_URI || "mongodb://localhost:27017/dsa_tracker";

	await mongoose.connect(MONGO_URI);
	console.log("✅  MongoDB connected");

	// Clear existing questions
	await Question.deleteMany({});
	console.log("🗑️   Cleared existing questions");

	// Insert all questions
	const inserted = await Question.insertMany(ALL_QUESTIONS);
	console.log(`✅  Inserted ${inserted.length} questions`);

	// Print topic summary
	const topics = [...new Set(ALL_QUESTIONS.map((q) => q.topicId))];
	topics.forEach((t) => {
		const count = ALL_QUESTIONS.filter((q) => q.topicId === t).length;
		const easy = ALL_QUESTIONS.filter(
			(q) => q.topicId === t && q.difficulty === "Easy",
		).length;
		const med = ALL_QUESTIONS.filter(
			(q) => q.topicId === t && q.difficulty === "Medium",
		).length;
		const hard = ALL_QUESTIONS.filter(
			(q) => q.topicId === t && q.difficulty === "Hard",
		).length;
		console.log(
			`   ${t.padEnd(15)} → ${count} questions (E:${easy} M:${med} H:${hard})`,
		);
	});
	console.log(
		`\n📊  Total: ${ALL_QUESTIONS.length} questions across ${topics.length} topics`,
	);

	await mongoose.disconnect();
	console.log("👋  Done!");
}

seed().catch((err) => {
	console.error(err);
	process.exit(1);
});
