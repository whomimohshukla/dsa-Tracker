async function seedAI() {
  try {
    await mongoose.connect(process.env.MONGODB_URI);
    console.log('✅ MongoDB connected');

    // Remove old AI track data only
    const deleted = await Question.deleteMany({ track: 'ai' });
    console.log(`🗑️  Removed ${deleted.deletedCount} old AI topics`);

    // Ensure DSA questions have track field
    const dsa = await Question.updateMany(
      { track: { $exists: false } },
      { $set: { track: 'dsa', phaseIndex: 0, tag: '' } }
    );
    if (dsa.modifiedCount > 0) {
      console.log(`🔧 Backfilled ${dsa.modifiedCount} DSA questions with track='dsa'`);
    }

    // Insert AI roadmap
    const inserted = await Question.insertMany(aiTopics);
    console.log(`\n🚀 Inserted ${inserted.length} AI/ML topics successfully!\n`);

    // Summary by phase
    const summary = {};
    for (const t of aiTopics) {
      summary[t.phase] = (summary[t.phase] || 0) + 1;
    }
    console.log('📊 Topics by Phase:');
    for (const [phase, count] of Object.entries(summary)) {
      console.log(`   ${phase}: ${count} topics`);
    }

    console.log('\n✨ AI/ML Roadmap seed complete!');
    console.log('   Run `node seed.js` if you also need to re-seed DSA questions.');
    process.exit(0);
  } catch (err) {
    console.error('❌ Seed failed:', err);
    process.exit(1);
  }
}

seedAI();